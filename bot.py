async def handle_wordgame(msg, cid: int, text: str, state: dict):
    bot_msg_id    = state.get("game_bot_msg_id", 0)
    last_bot_word = state.get("game_last_word", "")
    replied_to    = msg.reply_to_message
    if not replied_to or replied_to.message_id != bot_msg_id:
        return

    player_word = re.sub(r"[^\w]", "", text.strip().lower())
    if not player_word:
        return

    needed_letter = get_last_letter(last_bot_word)
    if not player_word.startswith(needed_letter):
        sent = await msg.reply_text(
            f"❌ Нужно слово на букву *{needed_letter.upper()}*!\n"
            f"Моё слово: *{last_bot_word}*\n_(отвечай на это сообщение)_",
            parse_mode="Markdown"
        )
        set_state(cid, game_bot_msg_id=sent.message_id)
        return

    if is_word_used(cid, player_word):
        sent = await msg.reply_text(
            f"❌ Слово *{player_word}* уже было! Это проигрыш.\n"
            f"Попробуй другое на букву *{needed_letter.upper()}*\n_(отвечай на это сообщение)_",
            parse_mode="Markdown"
        )
        set_state(cid, game_bot_msg_id=sent.message_id)
        return

    mark_word_used(cid, player_word)
    bot_letter = get_last_letter(player_word)
    bot_word   = get_bot_word(cid, bot_letter)

    if not bot_word:
        await msg.reply_text(
            f"✅ Принято: *{player_word}*\n\n"
            f"😵 Я не знаю слов на букву *{bot_letter.upper()}*... Ты выиграл!\n\n"
            f"Начать снова: /wordgame",
            parse_mode="Markdown"
        )
        set_state(cid, wordgame=0, game_last_word="", game_bot_msg_id=0)
        clear_used_words(cid)
        return

    mark_word_used(cid, bot_word)
    next_letter = get_last_letter(bot_word)
    sent = await msg.reply_text(
        f"✅ *{player_word}* — принято!\n\n"
        f"Моё слово: *{bot_word}*\n\n"
        f"Твоё слово на букву: *{next_letter.upper()}*\n_(отвечай на это сообщение)_",
        parse_mode="Markdown"
    )
    set_state(cid, game_last_word=bot_word, game_bot_msg_id=sent.message_id)

