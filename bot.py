def is_banned(chat_id: int, user) -> bool:
    if not user:
        return False
    targets = []
    if user.username:
        targets.append(user.username.lower())
    if user.first_name:
        targets.append(user.first_name.lower())
    with get_db() as db:
        for t in targets:
            row = db.execute(
                "SELECT 1 FROM banned_users WHERE chat_id=? AND target=?", (chat_id, t)
            ).fetchone()
            if row:
                return True
    return False

