def is_word_used(chat_id: int, word: str) -> bool:
    with get_db() as db:
        return db.execute(
            "SELECT 1 FROM used_words WHERE chat_id=? AND word=?", (chat_id, word.lower())
        ).fetchone() is not None

def mark_word_used(chat_id: int, word: str):
    with get_db() as db:
        db.execute("INSERT OR IGNORE INTO used_words (chat_id, word) VALUES (?, ?)", (chat_id, word.lower()))

def clear_used_words(chat_id: int):
    with get_db() as db:
        db.execute("DELETE FROM used_words WHERE chat_id=?", (chat_id,))

def get_bot_word(chat_id: int, letter: str) -> str | None:
    letter = letter.lower()
    words  = WORDS_BY_LETTER.get(letter, [])
    random.shuffle(words)
    for w in words:
        if not is_word_used(chat_id, w):
            return w
    return None

def get_last_letter(word: str) -> str:
    word = word.lower().strip()
    for ch in reversed(word):
        if ch not in ("ь", "ъ"):
            return ch
    return word[-1] if word else ""

