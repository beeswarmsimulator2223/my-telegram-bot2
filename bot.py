        for col in [
            "next_respond_at INTEGER DEFAULT 5",
            "wordgame INTEGER DEFAULT 0",
            "game_last_word TEXT DEFAULT ''",
            "game_bot_msg_id INTEGER DEFAULT 0",
        ]:
            try:
                db.execute(f"ALTER TABLE state ADD COLUMN {col}")
            except Exception:
                pass

