def forget_and_unlearn(chat_id: int, target: str):
    """Удаляет юзера и пересобирает мозг без его сообщений"""
    clean = target.lstrip("@").lower()

    # Найти user_id
    user_id = get_user_id_by_name(chat_id, clean)

    with get_db() as db:
        # Удаляем юзера из таблицы users
        db.execute(
            "DELETE FROM users WHERE chat_id=? AND (LOWER(username)=? OR LOWER(name)=?)",
            (chat_id, clean, clean)
        )
        # Удаляем его сообщения из raw_messages
        if user_id:
            db.execute(
                "DELETE FROM raw_messages WHERE chat_id=? AND user_id=?",
                (chat_id, user_id)
            )
        # Очищаем мозг полностью
        db.execute("DELETE FROM markov WHERE chat_id=?", (chat_id,))
        db.execute("DELETE FROM vocab  WHERE chat_id=?", (chat_id,))
        db.execute("DELETE FROM style  WHERE chat_id=?", (chat_id,))

        # Берём оставшиеся сообщения и пересобираем
        rows = db.execute(
            "SELECT text FROM raw_messages WHERE chat_id=?", (chat_id,)
        ).fetchall()

    # Пересобираем марков из оставшихся сообщений
    for row in rows:
        _learn_markov_raw(chat_id, row["text"])

def _learn_markov_raw(chat_id: int, text: str):
    """Внутренняя функция без записи в raw_messages — для пересборки"""
    words = text.lower().split()
    if len(words) < 2:
        return
    pairs   = list(zip(words, words[1:]))
    triples = [(f"{w1} {w2}", w3) for w1, w2, w3 in zip(words, words[1:], words[2:])] if len(words) >= 3 else []
    with get_db() as db:
        for key, nxt in pairs + triples:
            db.execute("""
                INSERT INTO markov (chat_id, key, next_word, count) VALUES (?, ?, ?, 1)
                ON CONFLICT(chat_id, key, next_word) DO UPDATE SET count=count+1
            """, (chat_id, key, nxt))
        for w in words:
            clean = re.sub(r"[^\w]", "", w)
            if clean:
                db.execute("""
                    INSERT INTO vocab (chat_id, word, freq) VALUES (?, ?, 1)
                    ON CONFLICT(chat_id, word) DO UPDATE SET freq=freq+1
                """, (chat_id, clean))

